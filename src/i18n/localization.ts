const catalogs: Record<AppLocale, Record<TranslationKey, string>> = { en, 'zh-CN': zhCN }
const CHINA_TIME_ZONES = new Set(['Asia/Shanghai', 'Asia/Urumqi', 'Asia/Chongqing', 'Asia/Harbin', 'Asia/Kashgar'])

export function translate(locale: AppLocale, key: TranslationKey, variables: TranslationVariables = {}) {
  return Object.entries(variables).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    catalogs[locale][key],
  )
}

export function normalizeLocale(value: string | null | undefined): AppLocale | null {
  if (!value) return null
  const normalized = value.toLowerCase()
  if (normalized === 'zh-cn' || normalized.startsWith('zh-hans') || normalized === 'zh') return 'zh-CN'
  if (normalized === 'en' || normalized.startsWith('en-')) return 'en'
  return null
}

export function resolveLocale(stored: string | null, browserLanguages: readonly string[], timeZone = 'UTC') {
  const savedLocale = normalizeLocale(stored)
  if (savedLocale) return savedLocale
  if (CHINA_TIME_ZONES.has(timeZone)) return 'zh-CN'
  return browserLanguages.map(normalizeLocale).find(Boolean) ?? 'en'
}

export function loadLocale(storage: Storage = localStorage, browserLanguages: readonly string[] = navigator.languages, timeZone = getLocalTimeZone()) {
  try {
    return resolveLocale(storage.getItem(LOCALE_STORAGE_KEY), browserLanguages, timeZone)
  } catch {
    return resolveLocale(null, browserLanguages, timeZone)
  }
}

export function saveLocale(locale: AppLocale, storage: Storage = localStorage) {
  try {
    storage.setItem(LOCALE_STORAGE_KEY, locale)
  } catch {
    // Keep language switching usable when storage is unavailable.
  }
}

export function getLocalTimeZone() {
  return Intl.DateTimeFormat().resolvedOptions().timeZone || 'UTC'
}

export function formatLocalizedDateTime(value: string, locale: AppLocale, timeZone: string) {
  const date = new Date(value)
  if (Number.isNaN(date.getTime())) return null
  return new Intl.DateTimeFormat(locale, {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
    timeZone,
    timeZoneName: 'short',
  }).format(date)
}

export function formatLocalizedList(values: string[], locale: AppLocale) {
  return new Intl.ListFormat(locale, { style: 'long', type: 'conjunction' }).format(values)
}
